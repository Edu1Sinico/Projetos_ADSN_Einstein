public class ConversaoUnidades {

    // Conversão de "pol" para "cm"
    public double polParaCm(double pol){
        double cm = pol * 2.54;
        return cm;
    }

    // Conversão "pés" para "cm"
    public double pesParaCm(double pes){
        double cm = pes * 30.48;
        return cm;
    }

    // Conversão "milhas" para "Km"
    public double milhaParaKm(double milha){
        double cm = 1.609;
        return cm;
    }

}
